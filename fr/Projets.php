	<h3>FootenFauteuil</h3>
		<h5> Date : Années 2016-2017 <br/>
			Travail : En groupe <br/><br/>
			Description : </h5>
		<p>L’objectif de ce projet est de concevoir un gant joystick afin d’améliorer la 
		maniabilité du fauteuil tout en gardant un prix de vente abordable pour le client. 
		En effet, même des personnes ayant un lourd handicap devraient avoir la possibilité
		de pratiquer ce sport. Néanmoins, les fauteuils adaptés sont soit américains, soit
		allemands et leur coût de vente est élevé. De plus, ils sont généralement munis d’un
		imposant joystick qui n’est pas toujours maniable pour une personne lourdement
		handicapée.<br/>
		La plus-value à ce projet sera donc de repenser le joystick qui permet de commander
		le fauteuil. L’enjeu est d’intégrer le joystick dans un gant qui permettra à toutes
		les personnes handicapées de l’utiliser tout en garantissant leur sécurité (la main
		de l’utilisateur doit rester en contact permanent avec le gant). La finalité du
		projet sera de concevoir un prototype fonctionnel aux alentours de mai. </p>
		
